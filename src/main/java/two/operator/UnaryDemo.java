package two.operator;

public class UnaryDemo {
    public static void main(String[] args) {
        int number = 23;
        number++;
        System.out.println(number);
        number = 23;
        ++number;
        System.out.println(number);
        number = 23;
        System.out.println(++number);
        number = 23;
        System.out.println(number++);
        System.out.println(number);
    }
}
